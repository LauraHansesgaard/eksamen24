        plan, hvilket belaster miljøet og spilder ressourcer som vand, energi
        og arbejdskraft. Ud over de økonomiske tab bidrager madspild også
        til store mængder drivhusgasudledninger, da den mad, der ikke spises,
        ofte ender på lossepladser, hvor den nedbrydes og udleder metan.
        Samtidig kunne meget af den spildte mad have været brugt til at bekæmpe
        sult og sikre bæredygtigere forbrugsmønstre.</p>
